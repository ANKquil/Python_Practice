def f22(x):
    a = x & 0b11
    b = x & 0b1111111100
    c = x & 0b10000000000
    d = x & 0b111111111100000000000
    e = x & 0b111111111000000000000000000000
    f = x & 0b1000000000000000000000000000000
    g = x & 0b10000000000000000000000000000000
    return f << 1 | a << 29 | d << 8 | c << 8 | e >> 12 | g >> 23 | b >> 2
